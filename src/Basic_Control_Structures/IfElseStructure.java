package Basic_Control_Structures;//选择语句 if-else 结构

public class IfElseStructure {
    public static void main(String[] args){
        int score = 60; // 及格分
        if(score >= 60){
            System.out.println("及格");
        }
        else {
            System.out.println("不及格");
        }
    }
}